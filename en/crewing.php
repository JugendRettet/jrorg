<h2 id="sar-positions">SAR POSITIONS</h2>
<p>- HOM | Deckmanager | RIB Driver | RIB Leader | RIB Mediator | Lilli driver | Lilli Leader | Doctor | Medic -</p>
<h3 id="head-of-mission">HEAD OF MISSION</h3>
<ul>
	<li>involved in the preparation and follow-up of the mission</li>
	<li>coordinates the cooperation of the individual SAR-departments (bridge, RIB, deck, Med team) during the mission</li>
	<li>is responsible for the safety of the crew as well as for the safety of the rescued people during the mission</li>
	<li>Coordinates and communicates with the Iuventa RIBs on scene</li>
	<li>permanent point of contact for crew, the organisation and external parties (other NGOs, state actors)</li>
	<li>Mission preparation:</li>
	<li>Establish the crew's operational readiness (allocation of positions / compilation of the teams, training / briefing)</li>
	<li>responsible for providing the necessary rescue equipment and functional PPE</li>
	<li>During the mission</li>
	<li>set the mission strategy</li>
	<li>in close cooperation with the Master / Deck management, make tactical decisions (strategy, usage of rescue equipment, embarkation and transfer of rescued people, etc.)</li>
	<li>controlling of the correct implementation of the SOP</li>
	<li>briefings / debriefings</li>
	<li>tactical agreements with other actors (NGOs, state actors)</li>
	<li>daily mission documentation</li>
	<li>Mission follow-up</li>
	<li>editing of the collected information - if necessary adaptation of SOP / mission strategy</li>
	<li>facilitating crew handover</li>
</ul>
<h4 id="profile">PROFILE</h4>
