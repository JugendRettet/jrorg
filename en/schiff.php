                            <p>Mohammad, 24 from Homs, Syria: „The revolution started in my city on the 15th March 2011. Firstly the university was closed, there I studied construction. By the 10th of March 2012 I fled to Egypt. I had to avoid being conscripted by the Syrian governments army. I knew that I would have to kill people there and that is something I am not willing to do. Yet life in Egypt was not easy. I had to work for small wages and everyday was a fight for survival. Andi t was not going to get better in the future. The life in Egypt is not easy fort he Syrians, yet also the Egyptians. Therefore the boat to Europe was my only way out."</p>
