                        <h4><img class="icon" src="./graphics/gru.svg" alt="Gründe">Gründe</h4>
                        <p>Warum haben wir <i>Jugend Rettet</i> gegründet?</p>
                        <p>Wir haben die zwei Vorsitzenden von <i>Jugend Rettet</i> und Menschen, die über das Mittelmeer geflüchtet sind diese Frage beantworten lassen.</p>
                        <div class="mehr-klappe"><br>
                            <!--

                            <div class="popup-img" style="background-image: url('./images/news.jpg');"></div>

