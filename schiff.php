                            <p>Jakob: „Ich kann die täglichen Meldungen über neue Schiffsunglücke nicht mehr stillschweigend hinnehmen. <i>Jugend Rettet</i> hat als Zusammenschluss junger Leute das Potenzial etwas zu verändern. Europaweit vernetzen wir motivierte Jugendliche und zeigen, wie wichtig es ist, einen gemeinsamen, europäischen Entwurf für eine humanere Asylpolitik zu finden. Unser Schiff ist keine langfristige Lösung, aber es wird Menschenleben retten und es wird die Frage aufwerfen, warum Jugendliche anstelle der europäischen Regierungen diese Aufgabe in privater Initiative übernehmen müssen.“</p><br>
                            <p>Mohammad, 24 aus Homs, Syrien: „Am 15. März 2011 begann die Revolution in meiner Stadt. Zuerst wurde die Universität geschlossen. Ich habe Bauwesen studiert. Am 10. März 2012 flüchtete ich nach Ägypten, um nicht von der syrischen Armee eingezogen zu werden. Ich wusste, wenn ich da hin muss, werde ich Menschen töten. Das wollte ich nicht. Doch das Leben in Ägypten ist schwer. Man arbeitet immer für einen sehr kleinen Lohn. Man kämpft jeden Tag für das Überleben und weiß, dass es hier nie besser wird. Es ist schwer für die Syrer und auch die Ägypter. Das Boot nach Europa war mein einziger Ausweg. Ich hatte keine andere Chance.“</p>
