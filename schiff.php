                    <div class="column mehr" id="sit">
                        <h4><img class="icon" src="./graphics/sit.svg" alt="Aktuelle Situation">Aktuelle Situation</h4>
                        <p>218.000 Menschen sind 2014 über das Mittelmeer geflüchtet. 3500 von ihnen haben es nicht geschafft.</p>
                        <div class="mehr-klappe"><br>
                            <p>In der Europäischen Union gilt das Grundrecht auf Asyl, allerdings gibt es kaum noch Wege Asyl legal zu beantragen, wodurch die Menschen gefährliche Fluchtrouten wie die über das Mittelmeer auf sich nehmen. (<a href="http://www.zeit.de/politik/2016-04/fluechtlingsrouten-europa-mittelmeer" target="_blank">ZEIT</a>, April 2016)</p><br>
                            <p>2015 kamen eine Million Menschen über das Mittelmeer in seeuntauglichen Booten nach Europa (<a href="https://www.uno-fluechtlingshilfe.de/nothilfe/europa.html" target="_blank">Angaben der UNO</a>, Mai 2016). Bei der gefährlichen Überfahrt sind 2015 3771 Menschen gestorben oder verschwunden, laut einer Schätzung des UN-Flüchtlingshilfswerks.</p><br>
                            <p>
                              2016 sind bereits 187.920 Flüchtlinge und Migranten auf dem Seeweg in Europa angekommen - davon 155.765 Personen in Griechenland und 31.252 in Italien (<a href="https://www.uno-fluechtlingshilfe.de/news/italien-1000-menschen-aus-seenot-gerettet-475.html" target="_blank">Angaben der UNO</a>, Mai 2016)
                            </p><br>
                            <p>
                              60% aller Jugendlichen wären bereit sich mit Gleichgesinnten für ihre politischen Interessen einzusetzen, wenn sie das Gefühl hätten, etwas verändern zu können (<a href="http://www.dgb.de/themen/++co++7525a922-220a-11df-7d31-00093d10fae2" target="_blank">DGB</a>, September 2009). Jeder 4. Jugendliche würde sich bei konkreten Projekten in der Stadt oder Gemeinde engagieren (<a href="http://www.spiegel.de/schulspiegel/leben/kinderhilfswerk-umfrage-kinder-wuerden-sich-gern-staerker-engagieren-a-890245.html" target="_blank">SPIEGEL Online</a>, März 2013). Aktuell gibt es keine vergleichbare von Jugendlichen initiierte Organisation mit dem Ziel der privaten Seenotrettung auf dem Mittelmeer. Konkrete, unabhängige Aktionsfelder für junge Erwachsene fehlen häufig, wodurch der Wille, sich politisch zu engagieren, oft nicht umgesetzt werden kann.
                            </p>
