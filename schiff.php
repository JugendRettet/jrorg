                    <div class="column">
                        <p>Wir sind eine Organisation von jungen Erwachsenen, die Gelder zur Seenotrettung auf dem Mittelmeer sammeln. Mit unserem Schiff, der IUVENTA, betreiben wir seit Juli 2016 private Seenotrettung auf dem Mittelmeer. Wir lokalisieren Menschen in Seenot und werden sie, je nach Situation, entweder durch Rettungsinseln sichern oder an Bord nehmen. Unser Ziel ist klar: weniger Tote im Mittelmeer. Darüber hinaus werden wir mit <a href="europe">Jugend Rettet europe</a> eine Diskussionsplattform über die europäische Asylpolitik für junge Erwachsene schaffen. Wir glauben an das Potenzial unserer jungen Generation, die aktuelle Situation durch eine breite asylpolitische Diskussion in ganz Europa zu verbessern.</p>
