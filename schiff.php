                        <h4>Das Schiff</h4>
                        <br>
                        <img src="./images/06-2016/IUVENTA1.jpg" alt="Jugend Rettet Rettungsschiff IUVENTA" style="max-width:100%">
                        <p>Im Mai 2016 haben wir unser Schiff gekauft, mit vielen Freiwilligen umgebaut und auf den Namen IUVENTA getauft.  Aus folgenden Gründen ist sie für Rettungsmissionen auf hoher See geeignet:</p>
                        <br>
                        <ul>
                          <li>&#10003; Die IUVENTA verfügt über eine große Fläche an Deck. So können wir, wenn es die Situation erfordert, 80-100 Menschen an Bord nehmen. Über die Deckfläche wird eine LKW-Plane gespannt sein, die die Menschen an Deck vor Sonneneinstrahlung oder Regen schützt. Im Winter wird diese Plane an den Seiten nach unten gespannt sein, wodurch ein geschlossener Innenbereich entsteht.</li>
                        </ul>
                        <img src="./images/06-2016/IUVENTA2.jpg" alt="Jugend Rettet Rettungsschiff IUVENTA" style="max-width:100%">
                        <br>
                        <ul>
                          <li>&#10003; Die IUVENTA zeichnet sich durch eine niedrige Bordwand aus. So können wir Menschen leicht und flexibel an Bord nehmen und auch unsere Beiboote werden bei einer niedrigen Bordwand leichter ins Wasser zu heben sein.</li>
                          <li>&#10003; Sie hat einen Hydraulikkran, der die Beiboote schnell ins Wasser heben kann. Wir haben zwei Beiboote an Bord. Bei einer Rettungsmission bleibt die IUVENTA in einiger Entfernung. Mit den Beibooten nähern wir uns von hinten links und rechts ran. Ein Beiboot bleibt immer vor Ort, um mit den Menschen zu kommunizieren. Das andere shuttelt die Menschen zu IUVENTA. So wird bei einer Rettung Panik vermieden.</li>
                          <li>&#10003; Unser Schiff hat Platz für ausreichend Frischwassertanks. Einige Menschen in Not müssen mehrere Tage ohne Wasser auf ihrem Boot verbringen. Um die Dehydrierung zu lindern, werden die Menschen mit Wasser versorgt.</li>
                        </ul>
